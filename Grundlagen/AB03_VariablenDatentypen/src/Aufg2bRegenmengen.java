public class Aufg2bRegenmengen {
    public static void main(String[] args) {

        double oktober = 126.4;
        double september = 192.9;
        double august = 241.1;

        double mittelwert = (oktober + september + august) / 3;

        System.out.println("Regenmenge Oktober: " + oktober);
        System.out.println("Regenmenge September: " + september);
        System.out.println("Regenmenge August: " + august);
        System.out.println("Mittelwert Regen: " + mittelwert);

    }
}
